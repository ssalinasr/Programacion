    public void table(){
        DefaultTableModel tb = new DefaultTableModel(){
              @Override
    public boolean isCellEditable(int row, int column) {
       //all cells false
       return false;
    }
        };
        tb.addColumn("Palabra");
        tb.addColumn("Significado");
        
        if(words.size() > 0){
        for(int i = 0 ; i < words.size(); i++){
            tb.addRow(new String[]{words.get(i),meanings.get(i)});
        }
        }
        
        this.ListaPalabras.setModel(tb);
        
    }
    
    public void buildLabels(){
        
        
            LinkedList<TrieNode> PreOrden = tr.getPreOrden();
            LinkedList<String> InOrden = tr.getInOrden();
            LinkedList<TrieNode> PosOrden = tr.getPostOrden();
            
            String pre = "PreOrden: ";
            String in = "InOrden: ";
            String pos = "PosOrden: ";
            
            for(TrieNode pr : PreOrden){
                pre += Character.toString(pr.content) + " ";
            }
            
            for(TrieNode po : PosOrden){
                pos += Character.toString(po.content) + " ";
            }
                        
            for(String i : InOrden){
                in += i + " ";
            }
            
            this.LblPreorden.setText(pre);
            this.LblInorden.setText(in);
            this.LblPosorden.setText(pos);
            
    }

