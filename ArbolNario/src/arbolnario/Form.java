    private void BtnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnInsertarActionPerformed
        String word_to_insert = this.TxtTexto.getText();
        String[] splitted = word_to_insert.split(",");
        
        if(tr.search(splitted[0])){
            JOptionPane.showMessageDialog(null, "La palabra "+splitted[0]+" ya existe.", "Información", 2);   
        }
        else{
            tr.insert(splitted[0], splitted[1]);
            words.add(splitted[0]);
            meanings.add(splitted[1]);
            JOptionPane.showMessageDialog(null, "La palabra "+splitted[0]+" ha sido insertada.", "Información", 1);   
            this.table();
            this.buildLabels();
        }
        
        tr.getInOrden();
        System.out.println("");
        this.TxtTexto.setText("");
    }//GEN-LAST:event_BtnInsertarActionPerformed

    private void BtnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRemoverActionPerformed
        String word_to_insert = this.TxtTexto.getText();
        String[] splitted = word_to_insert.split(",");
        
        if(tr.search(splitted[0])){
            tr.remove(splitted[0]);
            words.remove(splitted[0]);
            meanings.remove(splitted[1]);
            JOptionPane.showMessageDialog(null, "La palabra "+splitted[0]+" ha sido removida.", "Información", 1);
            this.table();
            this.buildLabels();
        }
        else{
            JOptionPane.showMessageDialog(null, "La palabra "+splitted[0]+" no existe.", "Información", 2);   
        }
        
        tr.getInOrden();
        System.out.println("");
        this.TxtTexto.setText("");
    }//GEN-LAST:event_BtnRemoverActionPerformed

